    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå PCM
    const pcmStats = statSync(pcmFilePath)
    if (pcmStats.size === 0) {
      throw new Error(`‡πÑ‡∏ü‡∏•‡πå PCM ‡∏ß‡πà‡∏≤‡∏á‡πÄ‡∏õ‡∏•‡πà‡∏≤: ${pcmFilePath}`)
    }

    console.log(`üìÅ ‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå PCM: ${pcmStats.size} bytes`)

    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå‡∏°‡∏µ‡∏Ç‡∏ô‡∏≤‡∏î‡∏ó‡∏µ‡πà‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏° (‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏ô‡πâ‡∏≠‡∏¢ 1KB)
    if (pcmStats.size < 1024) {
      console.log(`‚ö†Ô∏è ‡πÑ‡∏ü‡∏•‡πå PCM ‡∏°‡∏µ‡∏Ç‡∏ô‡∏≤‡∏î‡πÄ‡∏•‡πá‡∏Å‡πÄ‡∏Å‡∏¥‡∏ô‡πÑ‡∏õ: ${pcmStats.size} bytes`)
    }

    const reader = createReadStream(pcmFilePath)
    const writer = new wav.FileWriter(wavFilePath, {
      channels: 2,
      sampleRate: 48000,
      bitDepth: 16
    })

    await pipeline(reader, writer)
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡πÑ‡∏ü‡∏•‡πå WAV ‡∏ó‡∏µ‡πà‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡∏∂‡πâ‡∏ô
